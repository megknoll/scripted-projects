function divide(x,y)
{
	return x/y;
}

function multiply(x,y)
{
	return x*y;
}

function subtract(x,y)
{
	return x-y;
}

function add(x,y)
{
	return x+y;
}

function power(x,y)
{
	return Math.pow(x,y);
}

function square_root(x)
{
	return Math.sqrt(x);
}

function logBase10(x)
{
	return Math.log(x) / Math.LN10;
}

function generateRandomNumber()
{
	return Math.floor((Math.random() * 1000) + 1);
}
